const char* GetLangName( U16 langID )
{
    const size_t NUM_LANGS = sizeof( USBLangIDs ) / sizeof( IdNamePair );

    IdNamePair srch;
    srch.id = langID;

    const IdNamePair* res = std::lower_bound( USBLangIDs, USBLangIDs + NUM_LANGS, srch );

    if( res->id == langID && res->name != NULL )
        return res->name;

    return "<unknown>";
}

